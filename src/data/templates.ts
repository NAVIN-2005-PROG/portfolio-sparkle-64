/**
 * Template Data - 50+ Portfolio Templates
 * Each template includes customizable sections and styling
 */

export interface TemplateSection {
  id: string;
  type: 'hero' | 'about' | 'skills' | 'experience' | 'projects' | 'contact' | 'education' | 'services' | 'testimonials';
  title: string;
  content: Record<string, string | string[]>;
}

export interface PortfolioTemplate {
  id: number;
  name: string;
  category: string;
  color: string;
  rating: number;
  uses: number;
  description: string;
  sections: TemplateSection[];
  style: {
    primaryColor: string;
    secondaryColor: string;
    fontFamily: string;
    layout: 'modern' | 'classic' | 'minimal' | 'bold' | 'creative';
  };
}

export interface SavedPortfolio {
  id: string;
  templateId: number;
  templateName: string;
  name: string;
  createdAt: string;
  updatedAt: string;
  data: Record<string, unknown>;
  style: PortfolioTemplate['style'];
  shareLink: string;
}

